# --- HELPER FUNCTIONS ---

def get_coords(messages, cols, lines):
    num_lines = len(messages)
    start_y = (lines // 2) - (num_lines // 2)
    coords = {}
    for row_offset, text in enumerate(messages):
        target_y = start_y + row_offset
        start_x = (cols // 2) - (len(text) // 2)
        for col_offset, char in enumerate(text):
            if char != " ":
                coords[(target_y, start_x + col_offset)] = char
    return coords

def run_sequence_reveal(stages_list, build_gap=3):
    try:
        cols, lines = os.get_terminal_size()
    except OSError:
        cols, lines = 80, 24

    # --- AUTO-SELECT LOGO SIZE ---
    # We replace the placeholder in our list with the correct logo based on screen size
    final_stages = []
    for s in stages_list:
        if s == "AUTO_LOGO":
            # If screen is smaller than 100 wide or 40 tall, use SMALL
            if cols < 38 or lines < 20:
                final_stages.append(TINY_LOGO)
            elif cols < 100 or lines < 45:
                final_stages.append(SMALL_LOGO)
            else:
                final_stages.append(HUGE_LOGO)
        else:
            final_stages.append(s)

    print(CLEAR_SCREEN + HIDE_CURSOR + HOME)

    # Initial setup
    heads = {x: random.randint(-lines, 0) for x in range(1, cols + 1)}
    lengths = {x: random.randint(5, lines) for x in range(1, cols + 1)}
    
    current_idx = 0
    logo_coords = get_coords(final_stages[current_idx], cols, lines)
    frozen = set()
    stage_start = time.time()
    draining = False

    try:
        while True:
            now = time.time()
            
            # Stage Management
            if len(frozen) >= len(logo_coords) and (now - stage_start) > build_gap:
                current_idx += 1
                if current_idx < len(final_stages):
                    frozen = set()
                    logo_coords = get_coords(final_stages[current_idx], cols, lines)
                    stage_start = now
                else:
                    draining = True

            if draining and all(y - lengths[x] > lines for x, y in heads.items()):
                break

            for x in heads:
                y = heads[x]
                
                # Draw Logo
                if not draining and (y, x) in logo_coords:
                    print(f"{pos(y, x)}{WHITE}{B}{logo_coords[(y, x)]}{R}", end="")
                    frozen.add((y, x))
                
                # Draw Rain
                if 1 <= y <= lines:
                    if (y, x) not in frozen:
                        char = random.choice("0123456789ABCDEF")
                        print(f"{pos(y, x)}{LGREEN}{DM}{char}{R}", end="")
                    else:
                        print(f"{pos(y, x)}{WHITE}{B}{logo_coords[(y, x)]}{R}", end="")

                # Erase Tail
                tail_y = y - lengths[x]
                if 1 <= tail_y <= lines and (tail_y, x) not in frozen:
                    print(f"{pos(tail_y, x)} ", end="")

                heads[x] += 1
                if heads[x] - lengths[x] > lines:
                    heads[x] = (lines + 100) if draining else 0

            sys.stdout.flush()
            time.sleep(0.02)

        print(CLEAR_SCREEN + HOME + SHOW_CURSOR)
    finally:
        print(SHOW_CURSOR)

stages = [
    ["STARSHOT STUDIOS"],
    "AUTO_LOGO",
    [""]
    ]

if __name__ == "__main__":
    run_sequence_reveal(stages)